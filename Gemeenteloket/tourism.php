        <div class="watertoren"><p>The Water Tower found in Zoetermeer has been designed by the architect C. Visser. Construction started in 1927 and construction was finished in 1928. A foundation called ‘Stichting Drinkwaterleiding De Tien Gemeenten’ requested the construction.</p>
        <p>The water tower is 48,65 meters tall and the water reservoir is 500 m³ in total. The square around the Water Tower has been decorated with multiple stones, containing the names of the members of the foundation. Those being Wassenaar, Stompwijk, Zoetermeer, Zegwaart, Benthuizen, Moercapelle, Zevenhuizen, Bleiswijk, Bergschenhoek, Berkel, Pynacker, Nootdorp and Veur. All coats of arms can be seen around the entrance, except the ones from Stompwijk and Veur. Next to that, the build year and the province’s coat of arms can be seen as well.</p>
